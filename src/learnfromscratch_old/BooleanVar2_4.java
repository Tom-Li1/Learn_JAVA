public class BooleanVar2_4 {
    public static void main(String[] args) {
        int a = 2;
        int b = 3;
        boolean isMax = (a > b);
        boolean isMin = (a < b);

        System.out.println("a = 2 and b = 3");
        System.out.println(a + " > " + b + " = " + isMax);
        System.out.println(a + " < " + b + " = " + isMin);
    }
}
