public class VariableWork2_9 {
    public static void main(String[] args) {
        int numOne, numTwo;
        numOne = 1;
        System.out.println("Number one = " + numOne);

        numTwo = numOne;
        System.out.println("Number two = " + numTwo);

        int numThree = 2;
        System.out.println("Number three = " + numThree);

        float floatOne = 114.514f;
        System.out.println("Float one = " + floatOne);

        double doubleOne = 123.456;
        System.out.println("Double one = " + doubleOne);

        double doubleTwo = numTwo;
        System.out.println("Double two = " + doubleTwo);
    }
}
